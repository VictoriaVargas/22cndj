			function applyAjaxFileUploadComprobante(element) {
				$(element).AjaxFileUpload({
					action: "<?= base_url()?>Inicio/saveComprobante",
					onChange: function(filename) {
						// Create a span element to notify the user of an upload in progress
						var $span = $("<span />")
							.attr("class", $(this).attr("id"))
							.text("Uploading")
							.insertAfter($(this));
						$(this).remove();

						interval = window.setInterval(function() {
							var text = $span.text();
							if (text.length < 13) {
								$span.text(text + ".");
							} else {
								$span.text("Cargando");
							}
						}, 200);
					},
					onSubmit: function(filename) {					
						return true;
					},
					onComplete: function(filename, response) {
						$("#delay_imagen").removeAttr("disabled")
						window.clearInterval(interval);
						filename = response.name;
						$("#file_upload_alumno").val(filename);
						var $img = "<img id='file_comprobante' name='"+filename+"' src='<?= base_url()?>assets/comprobantes/"+filename + "' width='200px'>";
						var $span = $("span." + $(this).attr("id")).html($img),
							$fileInput = $("<input />")
								.attr({
									type: "file",
									name: $(this).attr("name"),
									id: $(this).attr("id")
								});
						if (typeof(response.error) === "string") {
							$span.replaceWith($fileInput);

							applyAjaxFileUploadComprobante($fileInput);
							alert(response.error);

							return;
						}
						$("<a />")
							.attr("href", "#;return false;")
							.text("x")
							.bind("click", function(e) {
								$span.replaceWith($fileInput);
								applyAjaxFileUploadComprobante($fileInput);
							})
							.appendTo($span);

						$(".img_preview_plano").css("background","url(uploads/"+filename );
						$(".img_preview_plano").css("background-size","cover")
					}
				});				
						}			

